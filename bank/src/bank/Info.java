package bank;

public class Info {
	String name, id, pw, account, phone, address;
	long money;

	public Info(String name, String id, String pw, String account, String phone, String address, long money) {
		this.name = name;
		this.id = id;
		this.pw = pw;
		this.account = account;
		this.phone = phone;
		this.address = address;
		this.money = money;
	}
}
